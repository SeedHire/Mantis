// runTaskBased executes the code stage as individual task-by-task prompts
// instead of one monolithic generation. The first task (usually setup/config)
// runs sequentially, then remaining tasks run in parallel batches.
// The TUI shows live animated spinners on running tasks and writes files immediately.
func runTaskBased(
	ctx context.Context,
	client *ollama.Client,
	codeModel string,
	systemPrompt string,
	userRequest string,
	planText string,
	tasks []Task,
	res *Result,
	root string,
	taskTimeout time.Duration,
	codeOpts *ollama.ModelOptions,
	onRL OnRateLimitFunc,
	projectCtx string,
) {
	// 10B: Detect language for parsed build error retries.
	taskLang := detectLang(root)

	// Atomic renderer owns all stdout for the task region.
	r := newTaskRenderer(tasks)
	r.initialRender()
	stopSpinner := r.startSpinner()
	defer stopSpinner()

	var mu sync.Mutex
	var writeMu sync.Mutex // protects file writes from parallel task races
	var allCode strings.Builder
	var totalPT, totalCT int
	var allWrittenFiles []string // tracks all files written across tasks
	buildCheckRoot := root           // used for autofix.Check; cleared if dep install fails
	var sealedManifest string    // populated after sequential tasks, injected into parallel workers
	// P9.5+Fix10: track failed tasks with file count — only cascade-skip on total failures (0 files).
	type failedTask struct{ fileCount int }
	failedTaskTitles := map[string]failedTask{}

	// defaultTaskTimeout is 8 minutes per task — generous but bounded.
	if taskTimeout <= 0 {
		taskTimeout = 8 * time.Minute
	}

	// safeWrite wraps extractAndApplyChanges with a mutex to prevent parallel tasks
	// from racing on the same files (Fix 2). Warnings are routed through the renderer.
	// On SEARCH mismatch, retries failed edits by re-prompting with actual file content.
	safeWrite := func(code, root string, taskMsgs []interface{}, onTok func(string)) []string {
		writeMu.Lock()
		defer writeMu.Unlock()
		paths, warnings := extractAndApplyChanges(code, root)

		// Check for failed edits and retry with actual file content.
		if failed := collectFailedEdits(code, root); failed != nil {
			retryPaths, _, _ := retryFailedEdits(ctx, client, codeModel, code, root, taskMsgs, codeOpts, onTok, onRL)
			if len(retryPaths) > 0 {
				paths = append(paths, retryPaths...)
				// Update warning: some edits recovered via retry.
				warnings = append(warnings, fmt.Sprintf("%d file(s) recovered via edit retry with actual content", len(retryPaths)))
			}
		}

		for _, w := range warnings {
			r.addWarning(w)
		}
		return paths
	}

	// runOneTask executes a single task and updates its status.
	runOneTask := func(i int, writtenFiles []string) {
		mu.Lock()
		tasks[i].StartTime = time.Now()
		atomic.StoreInt64(&tasks[i].streamTok, 0)
		mu.Unlock()
		r.setStatus(i, "running")

		// Each task gets its own deadline so a slow task doesn't starve siblings.
		taskCtx, taskCancel := context.WithTimeout(ctx, taskTimeout)
		defer taskCancel()

		taskPrompt := taskCodePrompt(userRequest, planText, tasks[i].Title, root, writtenFiles, sealedManifest, tasks[i].VerificationCriteria, projectCtx)
		msgs := []interface{}{
			ollama.Message{Role: "system", Content: systemPrompt + taskStageSuffix},
			ollama.Message{Role: "user", Content: taskPrompt},
		}

		// 10C: Per-task adaptive temperature based on task title.
		taskOpts := *codeOpts // shallow copy
		taskOpts.Temperature = codeTemperature(tasks[i].Title)

		var buf strings.Builder
		pt, ct, err := streamWithRetry(taskCtx, client, codeModel, msgs, &taskOpts, func(c string) {
			buf.WriteString(c)
			atomic.AddInt64(&tasks[i].streamTok, 1)
		}, onRL)

		mu.Lock()
		defer mu.Unlock()

		tasks[i].Elapsed = time.Since(tasks[i].StartTime)
		tasks[i].Tokens = pt + ct

		if err != nil {
			if partial := buf.String(); strings.TrimSpace(partial) != "" {
				tasks[i].Output = partial
				allCode.WriteString(partial)
				allCode.WriteString("\n\n")
				if root != "" {
					tokCb := func(c string) { atomic.AddInt64(&tasks[i].streamTok, 1) }
					written := safeWrite(partial, root, msgs, tokCb)
					tasks[i].FileCount = len(written)
					allWrittenFiles = append(allWrittenFiles, written...)
				}
			}
			r.setStatus(i, "failed")
			return
		}

		code := buf.String()
		tasks[i].Output = code
		allCode.WriteString(code)
		allCode.WriteString("\n\n")
		totalPT += pt
		totalCT += ct

		// Write files to disk immediately so user sees progress.
		var written []string
		if root != "" {
			tokCb := func(c string) { atomic.AddInt64(&tasks[i].streamTok, 1) }
			written = safeWrite(code, root, msgs, tokCb)
			tasks[i].FileCount = len(written)
			allWrittenFiles = append(allWrittenFiles, written...)
		}

		// Iterative fix loop: build check + content validation, up to 3 retries.
		if root != "" && len(written) > 0 {
			const maxTaskRetries = 3
			var lastBuildErr string
			lastCode := code

			for attempt := 0; attempt < maxTaskRetries; attempt++ {
				// Check build.
				buildResult := autofix.Check(buildCheckRoot, written)
				buildPassed := buildResult == nil || buildResult.Passed

				// Check content quality.
				valWarnings, stubRatio := validateContent(written)
				qualityOK := stubRatio <= 0.3

				if buildPassed && qualityOK {
					break // all good
				}

				// Build the retry prompt with all issues.
				var retryReason strings.Builder
				if !buildPassed {
					buildErrStr := buildResult.Output
					// Stuck detection: same build error twice means model can't fix it.
					if buildErrStr == lastBuildErr {
						r.setSubMessage(i, "stuck on same error — moving on")
						break
					}
					lastBuildErr = buildErrStr
					// 10B: Parse build errors for focused retry context.
					if parsed := parseBuildErrors(buildErrStr, taskLang); len(parsed) > 0 {
						retryReason.WriteString(buildRetryContext(root, parsed, 3) + "\n\n")
					} else {
						truncated := buildErrStr
						if len(truncated) > 2000 {
							truncated = truncated[:2000] + "\n... (truncated)"
						}
						retryReason.WriteString(fmt.Sprintf("Build failed:\n```\n%s\n```\n\n", truncated))
					}
				}
				if !qualityOK && len(valWarnings) > 0 {
					retryReason.WriteString("Code quality issues found:\n")
					for _, w := range valWarnings {
						retryReason.WriteString("- " + w + "\n")
					}
					retryReason.WriteString("\nReplace ALL placeholders and stubs with real implementations.\n")
				}

				r.setSubMessage(i, fmt.Sprintf("fixing issues — attempt %d/%d", attempt+1, maxTaskRetries))

				retryMsgs := []interface{}{
					msgs[0], msgs[1], // system + original user prompt
					ollama.Message{Role: "assistant", Content: lastCode},
					ollama.Message{Role: "user", Content: retryReason.String() +
						"Fix ONLY the broken parts. Use ```edit:filepath blocks with <<<SEARCH/===/>>>SEARCH markers for existing files. " +
						"Only use ```lang:filepath for brand new files."},
				}

				var retryBuf strings.Builder
				rpt, rct, rerr := streamWithRetry(taskCtx, client, codeModel, retryMsgs, codeOpts, func(c string) {
					retryBuf.WriteString(c)
					atomic.AddInt64(&tasks[i].streamTok, 1)
				}, onRL)
				if rerr != nil || strings.TrimSpace(retryBuf.String()) == "" {
					break // stream failed, keep what we have
				}

				retryCode := retryBuf.String()
				retryTokCb := func(c string) { atomic.AddInt64(&tasks[i].streamTok, 1) }
				retryWritten := safeWrite(retryCode, root, retryMsgs, retryTokCb)
				allWrittenFiles = append(allWrittenFiles, retryWritten...)
				tasks[i].FileCount += len(retryWritten)
				allCode.WriteString(retryCode)
				allCode.WriteString("\n\n")
				totalPT += rpt
				totalCT += rct
				tasks[i].Tokens += rpt + rct
				lastCode = retryCode

				// Update written files list for next iteration's check.
				if len(retryWritten) > 0 {
					written = append(written, retryWritten...)
				}
			}
		}

		r.clearSubMessage(i)
		r.setStatus(i, "done")
	}

	// skipTask marks a task as failed/skipped due to a prerequisite failure.
	skipTask := func(i int, reason string) {
		mu.Lock()
		tasks[i].Skipped = true
		mu.Unlock()
		r.setSubMessage(i, "skipped ("+reason+")")
		r.setStatus(i, "failed")
	}

	// Phase 1: Run first two tasks sequentially.
	// Task 0: project setup (package.json, config, tsconfig)
	// Task 1: data models / shared types — all parallel tasks depend on these.
	// Running them sequentially ensures stable type definitions before parallel work starts.
	seqCount := 2
	if len(tasks) < seqCount {
		seqCount = len(tasks)
	}

	for i := 0; i < seqCount; i++ {
		mu.Lock()
		snapshot := make([]string, len(allWrittenFiles))
		copy(snapshot, allWrittenFiles)
		mu.Unlock()
		runOneTask(i, snapshot)

		// P9.5: track failed sequential tasks — parallel tasks depend on them.
		mu.Lock()
		if tasks[i].Status == "failed" {
			failedTaskTitles[tasks[i].Title] = true
		}
		mu.Unlock()

		// Install dependencies after the first task (setup/config task).
		if i == 0 && root != "" {
			if !installDeps(ctx, root) {
				buildCheckRoot = "" // Fix 4: disable build checks when deps failed
			}
		}
	}

	// After sequential tasks, extract sealed types manifest so parallel workers
	// cannot redefine shared types/interfaces defined by task 0-1.
	if root != "" && len(allWrittenFiles) > 0 {
		mu.Lock()
		snapshot := make([]string, len(allWrittenFiles))
		copy(snapshot, allWrittenFiles)
		mu.Unlock()
		sealedManifest = extractSealedTypes(root, snapshot)
	}

	if len(tasks) > seqCount {
		// Phase 2: Run remaining tasks in parallel batches.
		const maxParallel = 3
		remaining := tasks[seqCount:]
		for batchStart := 0; batchStart < len(remaining); batchStart += maxParallel {
			batchEnd := batchStart + maxParallel
			if batchEnd > len(remaining) {
				batchEnd = len(remaining)
			}
			batch := remaining[batchStart:batchEnd]

			// Snapshot written files for this batch.
			mu.Lock()
			snapshot := make([]string, len(allWrittenFiles))
			copy(snapshot, allWrittenFiles)
			mu.Unlock()

			var wg sync.WaitGroup
			for j := range batch {
				taskIdx := seqCount + batchStart + j // index into original tasks slice

				// P9.5: skip if a prerequisite foundation task failed.
				mu.Lock()
				prereqFailed := len(failedTaskTitles) > 0
				var prereqName string
				for k := range failedTaskTitles {
					prereqName = k
					break
				}
				mu.Unlock()
				if prereqFailed {
					if len(prereqName) > 30 {
						prereqName = prereqName[:30] + "\u2026"
					}
					skipTask(taskIdx, "prerequisite failed: "+prereqName)
					continue
				}

				wg.Add(1)
				go func(idx int, files []string) {
					defer wg.Done()
					runOneTask(idx, files)
					mu.Lock()
					if tasks[idx].Status == "failed" {
						failedTaskTitles[tasks[idx].Title] = true
					}
					mu.Unlock()
				}(taskIdx, snapshot)
			}
			wg.Wait()
		}
	}

	stopSpinner()
	r.printSummary()

	res.PromptTok += totalPT
	res.ComplTok += totalCT
	res.CodeText = allCode.String()
	res.Tasks = tasks
	res.WrittenFiles = allWrittenFiles
	autoCommitWrites(root, allWrittenFiles, res.PlanText)
}

