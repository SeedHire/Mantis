// readPriorContext reads actual file content from disk for files written by prior tasks.
// This gives downstream tasks exact type signatures, interfaces, and exports to import from.
// Prioritizes model/type files first. Caps total output at maxChars.
func readPriorContext(root string, writtenFiles []string, maxChars int) string {
	if root == "" || len(writtenFiles) == 0 {
		return ""
	}

	// Sort: prioritize files with model/type/interface in path.
	type fileEntry struct {
		path     string
		priority int
	}
	entries := make([]fileEntry, 0, len(writtenFiles))
	for _, f := range writtenFiles {
		lower := strings.ToLower(f)
		p := 0
		for _, kw := range []string{"model", "type", "interface", "schema", "entity"} {
			if strings.Contains(lower, kw) {
				p = 1
				break
			}
		}
		entries = append(entries, fileEntry{path: f, priority: p})
	}
	// Stable sort: priority files first.
	for i := 1; i < len(entries); i++ {
		for j := i; j > 0 && entries[j].priority > entries[j-1].priority; j-- {
			entries[j], entries[j-1] = entries[j-1], entries[j]
		}
	}

	var sb strings.Builder
	sb.WriteString("\n\n## Already implemented files (import from these, don't recreate):\n")
	totalChars := 0
	const maxLinesPerFile = 300 // was 150 — models need more context to generate accurate SEARCH blocks

	for _, e := range entries {
		if totalChars >= maxChars {
			break
		}
		absPath := e.path
		if !filepath.IsAbs(absPath) {
			absPath = filepath.Join(root, absPath)
		}

		// Skip binary/generated/vendor paths.
		lower := strings.ToLower(absPath)
		if strings.Contains(lower, "node_modules") || strings.Contains(lower, ".git/") {
			continue
		}

		data, err := os.ReadFile(absPath)
		if err != nil {
			continue
		}
		content := string(data)

		// Skip likely binary files.
		if len(content) > 0 && strings.ContainsRune(content[:min(len(content), 512)], 0) {
			continue
		}

		// Fix 5: For type/model files (priority=1), read full content so type defs are visible.
		var snippet string
		if e.priority == 1 {
			snippet = content
		} else {
			lines := strings.SplitN(content, "\n", maxLinesPerFile+1)
			if len(lines) > maxLinesPerFile {
				lines = lines[:maxLinesPerFile]
			}
			snippet = strings.Join(lines, "\n")
		}

		// Determine relative path for display.
		relPath := e.path
		if filepath.IsAbs(relPath) {
			if rel, err := filepath.Rel(root, relPath); err == nil {
				relPath = rel
			}
		}

		exportSummary := extractExportSummary(content)
		block := fmt.Sprintf("\n### %s\n```\n%s\n```\n%s", relPath, snippet, exportSummary)
		if totalChars+len(block) > maxChars {
			break
		}
		sb.WriteString(block)
		totalChars += len(block)
	}

	return sb.String()
}

// exportRe matches lines that export symbols in JS/TS/Go/Python/CommonJS.
var exportRe = regexp.MustCompile(`(?m)^(?:export\s|func\s+[A-Z]|type\s+[A-Z]|class\s+\w|def\s+\w|module\.exports\s|exports\.\w)`)

// methodRe matches class method declarations (JS/TS async or sync methods).
var methodRe = regexp.MustCompile(`^\s+(?:async\s+)?([a-zA-Z_]\w*)\s*\(`)

// methodExclude filters out common non-method matches (constructors, control flow).
var methodExclude = map[string]bool{
	"constructor": true, "if": true, "for": true, "while": true,
	"switch": true, "catch": true, "return": true, "throw": true,
}

// extractMethodNames extracts public method names from class bodies in JS/TS/Python files.
// Returns a deduplicated list of method names found in the content.
func extractMethodNames(content string) []string {
	lines := strings.Split(content, "\n")
	var methods []string
	seen := map[string]bool{}
	inClass := false

	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		// Track class scope entry.
		if strings.HasPrefix(trimmed, "class ") {
			inClass = true
			continue
		}
		// Python method (indented def inside class).
		if inClass && strings.HasPrefix(trimmed, "def ") {
			// "def method_name(self, ...)"
			if idx := strings.Index(trimmed, "("); idx > 4 {
				name := strings.TrimSpace(trimmed[4:idx])
				if name != "__init__" && !strings.HasPrefix(name, "_") && !seen[name] {
					seen[name] = true
					methods = append(methods, name)
				}
			}
			continue
		}
		if !inClass {
			continue
		}
		// JS/TS method: "  async methodName(" or "  methodName("
		if m := methodRe.FindStringSubmatch(line); m != nil {
			name := m[1]
			if !methodExclude[name] && !seen[name] {
				seen[name] = true
				methods = append(methods, name)
			}
		}
	}
	return methods
}

// extractExportSummary scans file content for export/public declarations
// and returns a formatted summary. Helps downstream tasks use exact names.
// For CommonJS files with classes, also extracts method names so downstream
// tasks can call the correct methods without hallucinating names.
func extractExportSummary(content string) string {
	lines := strings.Split(content, "\n")
	var exports []string
	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if exportRe.MatchString(trimmed) {
			exports = append(exports, trimmed)
			if len(exports) >= 20 {
				break
			}
		}
	}

	methods := extractMethodNames(content)

	if len(exports) == 0 && len(methods) == 0 {
		return ""
	}
	var sb strings.Builder
	if len(exports) > 0 {
		sb.WriteString("**Exports (use these exact names):**\n")
		for _, e := range exports {
			sb.WriteString("- `")
			sb.WriteString(e)
			sb.WriteString("`\n")
		}
	}
	if len(methods) > 0 {
		sb.WriteString("**Methods (use these EXACT method names — do NOT invent alternatives):**\n")
		for _, m := range methods {
			sb.WriteString("- `")
			sb.WriteString(m)
			sb.WriteString("()`\n")
		}
	}
	sb.WriteString("\n")
	return sb.String()
}

// sealedTypePatterns matches exported type/interface/class/enum declarations across languages.
var sealedTypePatterns = []*regexp.Regexp{
	// TS/JS: export (interface|type|class|enum) Name
	regexp.MustCompile(`(?m)^export\s+(?:interface|type|class|enum)\s+(\w+)`),
	// TS/JS: export (const|let|var) Name
	regexp.MustCompile(`(?m)^export\s+(?:const|let|var)\s+(\w+)`),
	// TS/JS: export default class Name
	regexp.MustCompile(`(?m)^export\s+default\s+class\s+(\w+)`),
	// Go: type Name struct/interface
	regexp.MustCompile(`(?m)^type\s+([A-Z]\w*)\s+(?:struct|interface)\b`),
	// Go: func Name( — exported functions (capital first letter)
	regexp.MustCompile(`(?m)^func\s+([A-Z]\w*)\(`),
	// Python: class Name
	regexp.MustCompile(`(?m)^class\s+([A-Z]\w*)`),
	// Python: def Name — exported functions (capital first letter)
	regexp.MustCompile(`(?m)^def\s+([A-Z]\w*)`),
}

// extractSealedTypes scans files written by earlier tasks and extracts all exported
// type/interface/class/enum names into a "sealed types manifest" block. This block is
// injected into parallel worker prompts so models cannot redefine shared types.
// Returns "" if no types are found.
func extractSealedTypes(root string, writtenFiles []string) string {
	if root == "" || len(writtenFiles) == 0 {
		return ""
	}

	seen := map[string]bool{}
	var names []string

	for _, f := range writtenFiles {
		absPath := f
		if !filepath.IsAbs(absPath) {
			absPath = filepath.Join(root, absPath)
		}
		data, err := os.ReadFile(absPath)
		if err != nil {
			continue
		}
		content := string(data)
		for _, re := range sealedTypePatterns {
			for _, m := range re.FindAllStringSubmatch(content, -1) {
				name := m[1]
				if !seen[name] {
					seen[name] = true
					names = append(names, name)
				}
			}
		}
	}

	if len(names) == 0 {
		return ""
	}

	var sb strings.Builder
	sb.WriteString("\n\n## SEALED TYPES (from task 1 — data models/types task)\n")
	sb.WriteString("These types already exist on disk.\n")
	sb.WriteString("**DO NOT redefine any of the following. IMPORT them.**\n")
	for _, n := range names {
		sb.WriteString("- `")
		sb.WriteString(n)
		sb.WriteString("`\n")
	}
	sb.WriteString("\nViolating this rule means your output will be REJECTED.\n")
	return sb.String()
}

func taskCodePrompt(request, plan, taskTitle, root string, writtenFiles []string, sealedManifest string, criteria []string, projectCtx string) string {
	var sb strings.Builder
	sb.WriteString("## Original Request\n")
	sb.WriteString(request)
	sb.WriteString("\n\n## Full Plan\n")
	sb.WriteString(plan)
	sb.WriteString("\n\n## YOUR CURRENT TASK\nImplement ONLY this task: **")
	sb.WriteString(taskTitle)
	sb.WriteString("**\n\nOutput only the files needed for this specific task.")

	// Include explicit success criteria so the model knows exactly what "done" means.
	if len(criteria) > 0 {
		sb.WriteString("\n\n## Definition of Done\nThis task is complete ONLY when ALL of the following pass:\n")
		for _, c := range criteria {
			sb.WriteString("- " + c + "\n")
		}
	}

	if sealedManifest != "" {
		sb.WriteString(sealedManifest)
	}

	priorCtx := readPriorContext(root, writtenFiles, 36000)
	if priorCtx != "" {
		sb.WriteString(priorCtx)
	}
	if projectCtx != "" {
		sb.WriteString("\n\n")
		sb.WriteString(projectCtx)
	}
	return sb.String()
}

// assemble stitches the three stage outputs into clean markdown.
func assemble(plan, code, tests string) string {
	var sb strings.Builder
	sb.WriteString("## Plan\n\n")
	sb.WriteString(strings.TrimSpace(stripStagePreamble(plan, "###")))
	sb.WriteString("\n\n---\n\n## Implementation\n\n")
	sb.WriteString(strings.TrimSpace(stripStagePreamble(code, "```")))
	if strings.TrimSpace(tests) != "" {
		sb.WriteString("\n\n---\n\n## Tests\n\n")
		sb.WriteString(strings.TrimSpace(stripStagePreamble(tests, "```")))
	}
	return sb.String()
}

// editorValidate passes generated code through a lighter "editor" model that validates
// and cleans diff blocks before they're applied to disk. Source: Aider architect mode —
// strong reasoning model plans, cheap model applies edits. Returns the cleaned code text.
// Falls back to the original if the editor model fails or produces worse output.
func editorValidate(
	ctx context.Context,
	client *ollama.Client,
	editorModel string,
	codeText string,
	onRL OnRateLimitFunc,
) string {
	editorPrompt := `You are an edit validator. Your ONLY job is to verify and clean code blocks.

Rules:
1. Ensure every code block has the correct filepath annotation (e.g. ` + "```go:internal/auth/service.go" + `)
2. Ensure edit blocks have valid <<<SEARCH and >>>SEARCH markers with matching content
3. Fix any obvious syntax errors in the generated code (missing imports, unclosed brackets)
4. Do NOT change the logic or approach — only fix syntax and formatting
5. Output the corrected code blocks in the same format. If everything is correct, output them unchanged.

Output ONLY the corrected code blocks. No explanation.`

	msgs := []interface{}{
		ollama.Message{Role: "system", Content: editorPrompt},
		ollama.Message{Role: "user", Content: codeText},
	}

	editorOpts := &ollama.ModelOptions{
		Temperature: 0.05, // very low temp for mechanical validation
		NumCtx:      router.ContextWindowFor(editorModel),
	}

	var buf strings.Builder
	editorCtx, cancel := context.WithTimeout(ctx, 60*time.Second)
	defer cancel()

	_, _, err := streamWithRetry(editorCtx, client, editorModel, msgs, editorOpts, func(c string) { buf.WriteString(c) }, onRL)
	if err != nil {
		fmt.Printf("%s  ⚠ editor pass failed (%v) — using architect output directly%s\n", pColorDim, err, pColorReset)
		return codeText
	}

	cleaned := buf.String()
	// Sanity check: editor output should contain at least some code blocks.
	if strings.Count(cleaned, "```") < 2 {
		fmt.Printf("%s  ⚠ editor output too short — using architect output directly%s\n", pColorDim, pColorReset)
		return codeText
	}

	fmt.Printf("%s  ✓ editor validation pass complete%s\n", pColorDim, pColorReset)
	return cleaned
}

// SaveOutput writes the full pipeline result to .mantis/last-pipeline.md for reference.
// Returns an error if the write fails; callers should surface this to the user.
func SaveOutput(root, combined string) error {
	dir := filepath.Join(root, ".mantis")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	return os.WriteFile(filepath.Join(dir, "last-pipeline.md"), []byte(combined), 0o644)
}

// CompactSummary returns a short CLI-friendly summary of the pipeline result.
// It extracts the plan overview + file list, omitting verbose sections like
// Architecture, Task Breakdown, Risks, and Assumptions.
// When task-based execution was used, it includes per-task status.
func CompactSummary(res *Result) string {
	var sb strings.Builder

	// Extract plan overview (first ### section only).
	if overview := extractSection(res.PlanText, "Overview"); overview != "" {
		sb.WriteString(overview)
		sb.WriteString("\n")
	}

	// List files from the plan's "### Files" section (capped at 20 for readability).
	if files := extractSection(res.PlanText, "Files"); files != "" {
		lines := strings.Split(strings.TrimSpace(files), "\n")
		const maxFileLines = 20
		sb.WriteString("\n### Files\n\n")
		shown := 0
		for _, l := range lines {
			if strings.TrimSpace(l) == "" {
				continue
			}
			if shown >= maxFileLines {
				sb.WriteString(fmt.Sprintf("  … and %d more (see .mantis/last-pipeline.md)\n", len(lines)-maxFileLines))
				break
			}
			sb.WriteString(l)
			sb.WriteString("\n")
			shown++
		}
	}

	// Task-based execution: show per-task breakdown.
	if len(res.Tasks) > 0 {
		sb.WriteString("\n---\n\n### Tasks\n\n")
		done, failed, totalFiles := 0, 0, 0
		for _, t := range res.Tasks {
			switch t.Status {
			case "done":
				done++
				sb.WriteString(fmt.Sprintf("- ✓ %s", t.Title))
			case "failed":
				failed++
				sb.WriteString(fmt.Sprintf("- ✗ %s", t.Title))
			default:
				sb.WriteString(fmt.Sprintf("- ○ %s", t.Title))
			}
			if t.FileCount > 0 {
				sb.WriteString(fmt.Sprintf(" (%d files)", t.FileCount))
				totalFiles += t.FileCount
			}
			sb.WriteString("\n")
		}
		sb.WriteString(fmt.Sprintf("\n**%d/%d tasks completed** · **%d file(s)** written\n", done, len(res.Tasks), totalFiles))
		if failed > 0 {
			sb.WriteString(fmt.Sprintf("⚠ %d task(s) failed\n", failed))
		}
		sb.WriteString(fmt.Sprintf("\n> Full output saved to `.mantis/last-pipeline.md`\n"))
	} else {
		// Monolithic path: count code fences.
		codeFiles := countCodeFences(res.CodeText)
		testFiles := countCodeFences(res.TestText)
		if codeFiles > 0 || testFiles > 0 {
			sb.WriteString("\n---\n\n")
			if codeFiles > 0 {
				sb.WriteString(fmt.Sprintf("**%d implementation file(s)** written\n", codeFiles))
			}
			if testFiles > 0 {
				sb.WriteString(fmt.Sprintf("**%d test file(s)** written\n", testFiles))
			}
			sb.WriteString(fmt.Sprintf("\n> Full output saved to `.mantis/last-pipeline.md`\n"))
		}
	}

	return sb.String()
}

// extractSection pulls the content of a ### Section from plan text.
// BUG-5: case-insensitive match so "Task Breakdown" / "Task breakdown" / "TASK BREAKDOWN" all work.
func extractSection(plan, heading string) string {
	marker := "### " + heading
	lowerPlan := strings.ToLower(plan)
	lowerMarker := strings.ToLower(marker)
	idx := strings.Index(lowerPlan, lowerMarker)
	if idx < 0 {
		return ""
	}
	content := plan[idx+len(marker):]
	// Find the next ### heading or end.
	nextIdx := strings.Index(content, "\n### ")
	if nextIdx >= 0 {
		content = content[:nextIdx]
	}
	return strings.TrimSpace(content)
}

// retryFailedEdits re-prompts the model with actual file content for edits that
// failed SEARCH matching. Returns additional written paths and token counts.
// This is the highest-impact fix for SEARCH mismatch: the model generates accurate
// SEARCH text when it can see the real file content.
func retryFailedEdits(
	ctx context.Context,
	client *ollama.Client,
	model string,
	originalCode string,
	root string,
	msgs []interface{},
	opts *ollama.ModelOptions,
	onToken func(string),
	onRL OnRateLimitFunc,
) ([]string, int, int) {
	failed := collectFailedEdits(originalCode, root)
	if len(failed) == 0 {
		return nil, 0, 0
	}

	// Cap at 5 files to stay within token budget.
	var retryPrompt strings.Builder
	retryPrompt.WriteString("Some edits failed because the SEARCH text didn't match the actual file content. " +
		"Here are the actual file contents. Regenerate ONLY the failing edits using ```edit:filepath blocks.\n\n")

	count := 0
	for relPath, content := range failed {
		if count >= 5 {
			break
		}
		retryPrompt.WriteString(fmt.Sprintf("### %s (actual content)\n```\n%s\n```\n\n", relPath, content))
		count++
	}

	retryMsgs := []interface{}{
		msgs[0], // system
		ollama.Message{Role: "user", Content: retryPrompt.String()},
	}

	var buf strings.Builder
	pt, ct, err := streamWithRetry(ctx, client, model, retryMsgs, opts, func(c string) {
		buf.WriteString(c)
		if onToken != nil {
			onToken(c)
		}
	}, onRL)
	if err != nil || strings.TrimSpace(buf.String()) == "" {
		return nil, 0, 0
	}

	paths, _ := extractAndApplyChanges(buf.String(), root)
	return paths, pt, ct
}

// countCodeFences counts ```lang:filepath fenced blocks in text.
func countCodeFences(text string) int {
	return len(parseCodeBlocks(text))
}

// stripStagePreamble removes any chain-of-thought preamble the model writes before
// the expected first token. For plan stages, content should start at "###". For
// code/test stages, content should start at "```". Anything before that first
// marker is stripped.
func stripStagePreamble(text, marker string) string {
	idx := strings.Index(text, marker)
	if idx <= 0 {
		return text
	}
	// Only strip if the preamble is prose (not code-fence context).
	before := text[:idx]
	// If the preamble contains a newline followed by the marker, it's likely
	// legitimate content (e.g. a code block inside a plan). Only strip if the
	// preamble is purely on the first few lines (less than 8 lines).
	lines := strings.Split(strings.TrimSpace(before), "\n")
	if len(lines) > 20 {
		return text
	}
	return text[idx:]
}

