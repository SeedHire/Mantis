// parseTasks extracts individual tasks from the plan's "### Task Breakdown" section.
// Supports numbered lists (1. ...), bullet lists (- ...), and checkbox lists (- [ ] ...).
func parseTasks(planText string) []Task {
	section := extractSection(planText, "Task Breakdown")
	if section == "" {
		return nil
	}

	var tasks []Task
	taskRe := regexp.MustCompile(`(?m)^(?:\d+[\.\)]\s*|\-\s*(?:\[[ x]\]\s*)?|\*\s*)(.+)$`)
	for _, m := range taskRe.FindAllStringSubmatch(section, -1) {
		title := strings.TrimSpace(m[1])
		if title == "" {
			continue
		}
		// Skip sub-items (indented lines) — they're details, not top-level tasks.
		lineIdx := strings.Index(section, m[0])
		if lineIdx > 0 && (section[lineIdx-1] == ' ' || section[lineIdx-1] == '\t') {
			continue
		}
		tasks = append(tasks, Task{
			ID:     len(tasks) + 1,
			Title:  title,
			Status: "pending",
		})
	}
	// Attach verification criteria parsed from the plan's dedicated section.
	criteria := parseVerificationCriteria(planText)
	for i := range tasks {
		if c, ok := criteria[tasks[i].ID]; ok {
			tasks[i].VerificationCriteria = c
		}
	}
	return tasks
}

// parseVerificationCriteria extracts per-task verification checks from the plan's
// "### Verification Criteria" section. Returns a map of task number → []check.
// Expected format:
//
//	1.
//	- `go build ./...` passes
//	- .env.example has all required keys
//	2.
//	- All types exported, no import cycles
func parseVerificationCriteria(planText string) map[int][]string {
	section := extractSection(planText, "Verification Criteria")
	if section == "" {
		return nil
	}
	result := map[int][]string{}
	var currentTask int
	// UX-3: match both bare "1." and "1. Task Name" patterns.
	taskNumRe := regexp.MustCompile(`^(?:#+\s*)?(\d+)[.\):]?\s*`)
	bulletRe := regexp.MustCompile(`^\s*[-*•]\s+(.+)$`)
	for _, line := range strings.Split(section, "\n") {
		trimmed := strings.TrimSpace(line)
		if m := taskNumRe.FindStringSubmatch(trimmed); m != nil {
			n, err := strconv.Atoi(m[1])
			if err == nil {
				currentTask = n
			}
		} else if m := bulletRe.FindStringSubmatch(trimmed); m != nil && currentTask > 0 {
			result[currentTask] = append(result[currentTask], m[1])
		}
	}
	return result
}

