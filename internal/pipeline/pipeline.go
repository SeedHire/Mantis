// printDoneLine prints a fun completion line with elapsed time.
func printDoneLine(elapsed time.Duration) {
	verb := doneVerbs[rand.Intn(len(doneVerbs))]
	var timeStr string
	if elapsed >= time.Minute {
		m := int(elapsed.Minutes())
		s := int(elapsed.Seconds()) % 60
		timeStr = fmt.Sprintf("%dm %ds", m, s)
	} else {
		timeStr = fmt.Sprintf("%.1fs", elapsed.Seconds())
	}
	fmt.Printf("\n%s  ✻ %s in %s%s\n", pColorDim, verb, timeStr, pColorReset)
}

// writePipelineLog appends a structured entry to .mantis/pipeline.log for debugging.
func writePipelineLog(root, prompt string, res *Result, elapsed time.Duration) {
	logDir := filepath.Join(root, ".mantis")
	_ = os.MkdirAll(logDir, 0o755)
	logPath := filepath.Join(logDir, "pipeline.log")

	f, err := os.OpenFile(logPath, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0o644)
	if err != nil {
		return
	}
	defer f.Close()

	// Truncate prompt for log readability.
	p := prompt
	if len(p) > 120 {
		p = p[:117] + "..."
	}

	fmt.Fprintf(f, "\n── %s ── elapsed: %.1fs ──\n", time.Now().Format("2006-01-02 15:04:05"), elapsed.Seconds())
	fmt.Fprintf(f, "prompt: %s\n", p)
	fmt.Fprintf(f, "tokens: prompt=%d compl=%d\n", res.PromptTok, res.ComplTok)
	fmt.Fprintf(f, "files_written: %d\n", len(res.WrittenFiles))
	for _, s := range res.Stages {
		icon := "✓"
		if s.Status == "failed" {
			icon = "✗"
		} else if s.Status == "skipped" {
			icon = "○"
		}
		line := fmt.Sprintf("  %s %-10s [%s] %.1fs %dt", icon, s.Name, s.Model, s.Elapsed.Seconds(), s.Tokens)
		if s.Retries > 0 {
			line += fmt.Sprintf(" (%d retries)", s.Retries)
		}
		if s.Error != "" {
			errMsg := s.Error
			if len(errMsg) > 100 {
				errMsg = errMsg[:97] + "..."
			}
			line += " — " + errMsg
		}
		fmt.Fprintln(f, line)
	}
}

// progressTicker prints a live token counter + elapsed time on the current line while a stage runs.
// Call the returned stop function when the stage completes; it returns elapsed time.
func progressTicker(stage string) (incr func(), stop func() time.Duration) {
	var count int64
	done := make(chan struct{})
	start := time.Now()
	ar := render.New()

	if !ar.IsTTY() {
		incr = func() { atomic.AddInt64(&count, 1) }
		stop = func() time.Duration { return time.Since(start) }
		return
	}

	frames := []string{"⠋", "⠙", "⠹", "⠸", "⠼", "⠴", "⠦", "⠧", "⠇", "⠏"}

	incr = func() { atomic.AddInt64(&count, 1) }

	go func() {
		i := 0
		ticker := time.NewTicker(200 * time.Millisecond)
		defer ticker.Stop()
		for {
			select {
			case <-done:
				ar.Clear()
				return
			case <-ticker.C:
				n := atomic.LoadInt64(&count)
				elapsed := time.Since(start).Seconds()
				frame := i
				i++
				ar.Render(func(w io.Writer) {
					fmt.Fprintf(w, "%s  %s %s  %d tokens · %.1fs%s\n", pColorDim, frames[frame%len(frames)], stage, n, elapsed, pColorReset)
				})
			}
		}
	}()

	var once sync.Once
	stop = func() time.Duration {
		once.Do(func() { close(done) })
		return time.Since(start)
	}
	return
}

// OnRateLimitFunc is called when a 429/rate-limit is encountered.
// It should rotate the API key on the client and return true if a new key
// was activated, or false if all keys are exhausted.
type OnRateLimitFunc func(client *ollama.Client) bool

// Options controls pipeline execution behaviour.
type Options struct {
	AvailableModels []ollama.ModelInfo
	SkipTests       bool              // skip test generation for faster turnaround
	Root            string            // project root for build verification; empty = skip
	MaxRetries      int               // max CODE stage retries on build failure (default 3)
	PlanOnly        bool              // stop after PLAN stage and return for user approval
	TaskTimeout     time.Duration     // per-task timeout (default 8m); 0 = no individual deadline
	EditorModel     string            // 7.2: lighter model for validating/cleaning diff blocks; empty = skip editor pass
	TDDFirst        bool              // 7.8: generate tests before code (tests-as-specification)
	OnRateLimit     OnRateLimitFunc                  // called on 429 to rotate API key; nil = no rotation
	ClarifyCallback func([]ClarifyQuestion) *ClarifyResult // interactive question selector; nil = skip
}

// Result holds aggregated pipeline output and token counts.
type Result struct {
	PlanText     string
	CodeText     string
	TestText     string
	Combined     string   // assembled markdown, ready for renderResponse + extractAndWriteFiles
	PromptTok    int
	ComplTok     int
	Tasks        []Task   // parsed tasks with status (for TUI display)
	WrittenFiles []string // absolute paths of all files written during task execution
	TestsRan     bool     // UX-5: true if pipeline already ran test verification
	Stages       []StageResult // observability: per-stage outcomes
}

// StageResult records the outcome of a single pipeline stage.
type StageResult struct {
	Name    string
	Model   string
	Elapsed time.Duration
	Tokens  int    // prompt + completion
	Status  string // "ok", "failed", "skipped", "fallback"
	Error   string // non-empty on failure
	Retries int
}

// Summary returns a compact multi-line summary of all stage outcomes.
func (r *Result) Summary() string {
	if len(r.Stages) == 0 {
		return ""
	}
	var sb strings.Builder
	sb.WriteString("Pipeline summary:\n")
	totalTok := 0
	var totalElapsed time.Duration
	for _, s := range r.Stages {
		icon := "✓"
		if s.Status == "failed" {
			icon = "✗"
		} else if s.Status == "skipped" {
			icon = "○"
		} else if s.Status == "fallback" {
			icon = "⚠"
		}
		line := fmt.Sprintf("  %s %-12s [%s] %.1fs %dt", icon, s.Name, s.Model, s.Elapsed.Seconds(), s.Tokens)
		if s.Retries > 0 {
			line += fmt.Sprintf(" (%d retries)", s.Retries)
		}
		if s.Error != "" {
			errMsg := s.Error
			if len(errMsg) > 80 {
				errMsg = errMsg[:77] + "..."
			}
			line += " — " + errMsg
		}
		sb.WriteString(line + "\n")
		totalTok += s.Tokens
		totalElapsed += s.Elapsed
	}
	sb.WriteString(fmt.Sprintf("  Total: %.1fs · %d tokens · %d files written\n", totalElapsed.Seconds(), totalTok, len(r.WrittenFiles)))
	return sb.String()
}

// Task represents a single implementation task parsed from the plan.
type Task struct {
	ID                   int
	Title                string
	Status               string // "pending", "running", "done", "failed"
	SubMessage           string // indented sub-line (e.g. "fixing issues — attempt 2/3")
	Skipped              bool   // true if skipped due to prerequisite failure
	Output               string // generated code for this task
	FileCount            int    // number of files written to disk for this task
	StartTime            time.Time
	Elapsed              time.Duration
	Tokens               int      // final token count (prompt + completion)
	streamTok            int64    // live streaming token counter (atomic)
	VerificationCriteria []string // explicit "done" checks from ### Verification Criteria
}

// ShouldRun returns true when the message is complex enough to warrant the pipeline.
// Only triggers on multi-component build/implement requests; simple fixes use the
// normal single-model path.
func ShouldRun(intent router.Intent, message string) bool {
	switch intent.Tier {
	case router.TierMax, router.TierTrivial, router.TierFast, router.TierVision:
		return false
	}
	return isComplexBuild(strings.ToLower(message))
}

// Run executes the 3-stage pipeline and returns the assembled result.
// systemPrompt is the full base prompt (identity + skills + brain context).
func Run(
	ctx context.Context,
	client *ollama.Client,
	userRequest string,
	systemPrompt string,
	opts Options,
) (*Result, error) {

	pipelineStart := time.Now()

	// 9E: Git stash checkpoint — save working state before pipeline modifies files.
	var stashRef string
	if opts.Root != "" {
		stashRef = gitStashCheckpoint(opts.Root)
	}

	planModel := router.ModelFor(router.TierReason)
	codeModel := router.ModelFor(router.TierCode)
	// 7.2: Resolve editor model — defaults to TierFast if not explicitly set.
	editorModel := opts.EditorModel
	if editorModel == "" {
		editorModel = router.ModelFor(router.TierFast)
	}
	// BUG-1: local options — no package-level mutation.
	planOpts := optsFor(planModel, 0.3)
	codeOpts := optsFor(codeModel, codeTemperature(userRequest))
	onRL := opts.OnRateLimit // key rotation callback (nil = disabled)
	res := &Result{}

	// 7.9: Model-aware edit format selection.
	editFormat := router.EditFormatForTier(router.TierCode)
	lang := detectLang(opts.Root)
	userConstraints := extractConstraints(userRequest)
	effCodeSuffix := codeStageSuffixFor(lang, editFormat) + userConstraints

	// Language-aware additions to plan and test suffixes.
	effPlanSuffix := planStageSuffix + langPlanRules(lang) + userConstraints
	effTestNaming := langTestNaming(lang)

	// ── Stage 1: PLAN ─────────────────────────────────────────────────────────
	fmt.Printf("%s  ◆ planning   [%s]%s\n", pColorDim, planModel, pColorReset)

	// Scan project structure so the model knows the language/framework/layout.
	projCtx := projectScanContext(opts.Root)

	planMsgs := []interface{}{
		ollama.Message{Role: "system", Content: systemPrompt + effPlanSuffix},
		ollama.Message{Role: "user", Content: planUserPrompt(userRequest) + projCtx},
	}
	var planBuf strings.Builder
	planIncr, planStop := progressTicker("planning")
	pt, ct, err := streamWithRetry(ctx, client, planModel, planMsgs, planOpts, func(c string) { planBuf.WriteString(c); planIncr() }, onRL)
	planElapsed := planStop()
	if err != nil {
		// Plan model unavailable — fall back to code model so the pipeline still runs.
		fmt.Printf("\n%s  ⚠ plan model unavailable, falling back to %s%s\n", pColorDim, codeModel, pColorReset)
		planBuf.Reset()
		planIncr2, planStop2 := progressTicker("planning")
		pt, ct, err = streamWithRetry(ctx, client, codeModel, planMsgs, codeOpts, func(c string) { planBuf.WriteString(c); planIncr2() }, onRL)
		planElapsed = planStop2()
		if err != nil {
			return nil, fmt.Errorf("plan stage: %w", err)
		}
		planModel = codeModel // record the actual model used
	}
	res.PlanText = planBuf.String()
	res.PromptTok += pt
	res.ComplTok += ct
	planStage := StageResult{Name: "plan", Model: planModel, Elapsed: planElapsed, Tokens: pt + ct, Status: "ok"}
	fmt.Printf("%s  ✓ plan ready  %s(%.1fs · %d tokens)%s\n", pColorGold, pColorDim, planElapsed.Seconds(), pt+ct, pColorReset)

	// ── Clarify: detect interactive questions from the model ─────────────────
	if clarifyQs, hasClarify := parseClarifyBlock(res.PlanText); hasClarify && opts.ClarifyCallback != nil {
		fmt.Printf("%s  ◆ %d clarifying question(s) detected%s\n", pColorDim, len(clarifyQs), pColorReset)
		if answers := opts.ClarifyCallback(clarifyQs); answers != nil {
			// Re-run plan stage with user answers injected.
			answerText := formatAnswers(answers)
			planMsgs = append(planMsgs,
				ollama.Message{Role: "assistant", Content: res.PlanText},
				ollama.Message{Role: "user", Content: answerText},
			)
			var replanBuf strings.Builder
			replanIncr, replanStop := progressTicker("re-planning")
			rpt, rct, rerr := streamWithRetry(ctx, client, planModel, planMsgs, planOpts, func(c string) { replanBuf.WriteString(c); replanIncr() }, onRL)
			replanElapsed := replanStop()
			if rerr == nil && strings.TrimSpace(replanBuf.String()) != "" {
				res.PlanText = replanBuf.String()
				res.PromptTok += rpt
				res.ComplTok += rct
				fmt.Printf("%s  ✓ plan updated  %s(%.1fs · %d tokens)%s\n", pColorGold, pColorDim, replanElapsed.Seconds(), rpt+rct, pColorReset)
			} else if rerr != nil {
				fmt.Printf("%s  ⚠ re-plan failed, using original plan%s\n", pColorDim, pColorReset)
			}
		} else {
			fmt.Printf("%s  ○ questions skipped — using model defaults%s\n", pColorDim, pColorReset)
		}
	}

	res.Stages = append(res.Stages, planStage)

	// Plan Mode: stop after PLAN stage for user review.
	if opts.PlanOnly {
		res.Combined = res.PlanText
		printDoneLine(time.Since(pipelineStart))
		return res, nil
	}

	// ── 7.8: TDD-first mode — generate tests before code ─────────────────────
	if opts.TDDFirst && !opts.SkipTests {
		fmt.Printf("%s  ◆ tdd-tests [%s] — tests-as-specification%s\n", pColorDim, codeModel, pColorReset)
		tddMsgs := []interface{}{
			ollama.Message{Role: "system", Content: systemPrompt + strings.ReplaceAll(testStageSuffix, "{TEST_NAMING}", effTestNaming)},
			ollama.Message{Role: "user", Content: "Write comprehensive tests for this plan. These tests define the specification — the implementation will be written to pass them.\n\n" + res.PlanText},
		}
		var tddBuf strings.Builder
		tddIncr, tddStop := progressTicker("tdd-tests")
		pt, ct, err := streamWithRetry(ctx, client, codeModel, tddMsgs, codeOpts, func(c string) { tddBuf.WriteString(c); tddIncr() }, onRL)
		tddElapsed := tddStop()
		if err == nil && strings.TrimSpace(tddBuf.String()) != "" {
			res.TestText = tddBuf.String()
			res.PromptTok += pt
			res.ComplTok += ct
			fmt.Printf("%s  ✓ tdd-tests ready  %s(%.1fs · %d tokens)%s\n", pColorGold, pColorDim, tddElapsed.Seconds(), pt+ct, pColorReset)
			if opts.Root != "" {
				written, eacWarns := extractAndApplyChanges(res.TestText, opts.Root)
				for _, w := range eacWarns {
					fmt.Printf("%s  ⚠ %s%s\n", pColorDim, w, pColorReset)
				}
				if len(written) == 0 {
					fmt.Printf("%s  ⚠ tdd-tests: no test files could be written to disk%s\n", pColorDim, pColorReset)
				}
			}
		} else {
			fmt.Printf("%s  ⚠ tdd-tests failed — falling back to code-first mode%s\n", pColorDim, pColorReset)
			if err != nil {
				fmt.Printf("%s    %v%s\n", pColorDim, err, pColorReset)
			}
		}
	}

	// ── Stage 1.5: Extract project context from plan ────────────────────────
	// Read existing files mentioned in the plan so the CODE stage sees real
	// imports, types, and function signatures — not guessing blind.
	planFiles := extractPlanFiles(res.PlanText)
	projectCtx := readProjectContext(opts.Root, planFiles, 15, 48000)

	// ── Stage 2: CODE — task-based or monolithic ─────────────────────────────
	// Try to parse discrete tasks from the plan. If found, execute them
	// one-by-one with focused prompts and live TUI progress. Otherwise
	// fall back to the monolithic code stage.
	tasks := parseTasks(res.PlanText)

	if len(tasks) >= 2 {
		// Task-based execution: each task gets its own focused prompt.
		fmt.Printf("%s  ◆ coding   [%s] — %d tasks%s\n", pColorDim, codeModel, len(tasks), pColorReset)
		codeStart := time.Now()
		runTaskBased(ctx, client, codeModel, systemPrompt, userRequest, res.PlanText, tasks, res, opts.Root, opts.TaskTimeout, codeOpts, onRL, projectCtx)
		codeElapsed := time.Since(codeStart)

		done := 0
		for _, t := range tasks {
			if t.Status == "done" {
				done++
			}
		}
		fmt.Printf("%s  ✓ code ready  %s(%d/%d tasks · %.1fs · %d tokens)%s\n",
			pColorGold, pColorDim, done, len(tasks), codeElapsed.Seconds(), res.PromptTok+res.ComplTok, pColorReset)
		codeStatus := "ok"
		if done < len(tasks) {
			codeStatus = "failed"
		}
		res.Stages = append(res.Stages, StageResult{Name: "code", Model: codeModel, Elapsed: codeElapsed, Tokens: res.PromptTok + res.ComplTok - planStage.Tokens, Status: codeStatus})

		// 8.5.1: Write structured progress file when the pipeline has 5+ tasks.
		if len(tasks) >= 5 && opts.Root != "" {
			writeProgressFile(opts.Root, userRequest, tasks)
		}
	} else {
		// Monolithic fallback for plans without clear task breakdown.
		fmt.Printf("%s  ◆ coding   [%s]%s\n", pColorDim, codeModel, pColorReset)

		maxRetries := opts.MaxRetries
		if maxRetries <= 0 {
			maxRetries = 3
		}

		codeMsgs := []interface{}{
			ollama.Message{Role: "system", Content: systemPrompt + effCodeSuffix},
			ollama.Message{Role: "user", Content: codeUserPrompt(userRequest, res.PlanText, projectCtx)},
		}

		var lastBuildErr string
		codeStart := time.Now()
		var codeTokTotal int
		var allWritten []string       // Fix 6: accumulate across retries
		writtenSeen := map[string]bool{}
		for attempt := 0; attempt <= maxRetries; attempt++ {
			var codeBuf strings.Builder
			codeIncr, codeStop := progressTicker("coding")
			pt, ct, err := streamWithRetry(ctx, client, codeModel, codeMsgs, codeOpts, func(c string) { codeBuf.WriteString(c); codeIncr() }, onRL)
			codeStop()
			if partial := codeBuf.String(); strings.TrimSpace(partial) != "" {
				res.CodeText = partial
			}
			if err != nil {
				if strings.Contains(err.Error(), "deadline exceeded") && len(res.CodeText) > 500 {
					res.PromptTok += pt
					res.ComplTok += ct
					codeTokTotal += pt + ct
					fmt.Printf("%s  ⚠ code stage timed out but captured %d chars of partial output%s\n",
						pColorDim, len(res.CodeText), pColorReset)
					break
				}
				return res, fmt.Errorf("code stage: %w", err)
			}
			res.CodeText = codeBuf.String()
			res.PromptTok += pt
			res.ComplTok += ct
			codeTokTotal += pt + ct

			// 9D: Validate output before writing to disk.
			if ok, reason := validateCodeOutput(res.CodeText); !ok {
				fmt.Printf("%s  ⚠ invalid code output: %s — retrying%s\n", pColorDim, reason, pColorReset)
				if attempt < maxRetries {
					codeMsgs = append(codeMsgs,
						ollama.Message{Role: "assistant", Content: res.CodeText},
						ollama.Message{Role: "user", Content: "Your output was invalid: " + reason + ". You MUST output code in ```lang:filepath fences for new files or ```edit:filepath fences for existing files. Do not explain — just write the code."},
					)
					continue
				}
				break
			}

			if opts.Root == "" || attempt == maxRetries {
				break
			}
			written, eacWarns := extractAndApplyChanges(res.CodeText, opts.Root)
			for _, ew := range eacWarns {
				fmt.Printf("%s  ⚠ %s%s\n", pColorDim, ew, pColorReset)
			}
			// Dep guardrails: warn about unnecessary dependencies.
			for _, dw := range checkSuspiciousDeps(res.CodeText, userRequest) {
				fmt.Printf("%s  %s%s\n", pColorDim, dw, pColorReset)
			}
			// Retry failed SEARCH edits with actual file content.
			if failed := collectFailedEdits(res.CodeText, opts.Root); failed != nil {
				retryPaths, rpt, rct := retryFailedEdits(ctx, client, codeModel, res.CodeText, opts.Root,
					codeMsgs, codeOpts, nil, onRL)
				written = append(written, retryPaths...)
				res.PromptTok += rpt
				res.ComplTok += rct
				codeTokTotal += rpt + rct
			}
			// Fix 6: accumulate all written files, deduped.
			for _, w := range written {
				if !writtenSeen[w] {
					allWritten = append(allWritten, w)
					writtenSeen[w] = true
				}
			}
			if len(written) == 0 {
				break
			}
			buildResult := autofix.Check(opts.Root, written)
			if buildResult == nil || buildResult.Passed {
				res.WrittenFiles = allWritten
				break
			}

			buildErrStr := buildResult.Output
			if buildErrStr == lastBuildErr {
				errPreview := buildErrStr
				if len(errPreview) > 200 {
					errPreview = errPreview[:200] + "…"
				}
				fmt.Printf("%s  [stuck: same build error twice — stopping retry]%s\n", pColorDim, pColorReset)
				fmt.Printf("%s  %s%s\n", pColorDim, errPreview, pColorReset)
				break
			}
			lastBuildErr = buildErrStr
			fmt.Printf("%s  [build error — retry %d/%d]%s\n", pColorDim, attempt+1, maxRetries, pColorReset)

			// 10B: Parse build errors and construct focused retry context.
			var retryMsg string
			if parsed := parseBuildErrors(buildErrStr, lang); len(parsed) > 0 {
				retryMsg = buildRetryContext(opts.Root, parsed, 3)
			} else {
				truncated := buildErrStr
				if len(truncated) > 2000 {
					truncated = truncated[:2000] + "\n... (truncated)"
				}
				retryMsg = fmt.Sprintf(
					"Build failed with the following error:\n\n```\n%s\n```\n\n"+
						"Fix only the affected function(s). Use ```edit:filepath blocks with <<<SEARCH/===/>>>SEARCH markers "+
						"to patch existing files. Only use ```lang:filepath for brand new files.",
					truncated)
			}
			if len(codeMsgs) > 2 {
				codeMsgs = codeMsgs[:2]
			}
			codeMsgs = append(codeMsgs,
				ollama.Message{Role: "assistant", Content: res.CodeText},
				ollama.Message{Role: "user", Content: retryMsg},
			)
		}
		// Fix 6: always set WrittenFiles from accumulated list.
		if len(allWritten) > 0 && len(res.WrittenFiles) == 0 {
			res.WrittenFiles = allWritten
		}
		codeElapsed := time.Since(codeStart)
		fmt.Printf("%s  ✓ code ready  %s(%.1fs · %d tokens)%s\n", pColorGold, pColorDim, codeElapsed.Seconds(), codeTokTotal, pColorReset)
		res.Stages = append(res.Stages, StageResult{Name: "code", Model: codeModel, Elapsed: codeElapsed, Tokens: codeTokTotal, Status: "ok"})
	}

	// ── Stage 2.5: Editor validation pass (7.2) ─────────────────────────────
	// Run the lighter editor model to validate/clean diff blocks before
	// the test stage. Only runs when an editor model is available and
	// the code model is different from the editor model (no point validating with itself).
	if editorModel != "" && editorModel != codeModel && res.CodeText != "" {
		fmt.Printf("%s  ◆ editor pass   [%s]%s\n", pColorDim, editorModel, pColorReset)
		res.CodeText = editorValidate(ctx, client, editorModel, res.CodeText, onRL)
		// Re-apply validated code to disk. Allow overwriting files from the code stage.
		if opts.Root != "" {
			pipelineWritten := map[string]bool{}
			for _, p := range res.WrittenFiles {
				if rel, err := filepath.Rel(opts.Root, p); err == nil {
					pipelineWritten[rel] = true
				}
			}
			_, eacWarns := extractAndApplyChanges(res.CodeText, opts.Root, pipelineWritten)
			for _, ew := range eacWarns {
				fmt.Printf("%s  ⚠ %s%s\n", pColorDim, ew, pColorReset)
			}
			// Retry failed SEARCH edits at the editor pass.
			if failed := collectFailedEdits(res.CodeText, opts.Root); failed != nil {
				retryPaths, _, _ := retryFailedEdits(ctx, client, codeModel, res.CodeText, opts.Root,
					[]interface{}{ollama.Message{Role: "system", Content: systemPrompt}}, codeOpts, nil, onRL)
				if len(retryPaths) > 0 {
					fmt.Printf("%s  ◉ %d file(s) recovered via edit retry%s\n", pColorDim, len(retryPaths), pColorReset)
				}
			}
		}
	}

	// ── Stage 3: TESTS in parallel ────────────────────────────────────────────
	type stageOut struct {
		text    string
		pt, ct  int
		elapsed time.Duration
		err     error
	}
	testCh := make(chan stageOut, 1)

	// BUG-6: capture planText before goroutine to avoid concurrent access to res.
	capturedPlanText := res.PlanText
	go func() {
		defer func() {
			if r := recover(); r != nil {
				testCh <- stageOut{err: fmt.Errorf("test stage panic: %v", r)}
			}
		}()
		if opts.SkipTests || opts.TDDFirst {
			testCh <- stageOut{} // TDD: tests already generated in pre-code stage
			return
		}
		fmt.Printf("%s  ◆ testing   [%s]%s\n", pColorDim, codeModel, pColorReset)
		msgs := []interface{}{
			ollama.Message{Role: "system", Content: systemPrompt + strings.ReplaceAll(testStageSuffix, "{TEST_NAMING}", effTestNaming)},
			ollama.Message{Role: "user", Content: testUserPrompt(userRequest, capturedPlanText)},
		}
		var buf strings.Builder
		testIncr, testStop := progressTicker("testing")
		pt, ct, err := streamWithRetry(ctx, client, codeModel, msgs, codeOpts, func(c string) { buf.WriteString(c); testIncr() }, onRL)
		testElapsed := testStop()
		testCh <- stageOut{buf.String(), pt, ct, testElapsed, err}
	}()

	// UX-4: select with ctx.Done() to avoid deadlock on Ctrl+C.
	var testOut stageOut
	select {
	case testOut = <-testCh:
	case <-ctx.Done():
		testOut = stageOut{err: ctx.Err()}
	}

	if testOut.err == nil && strings.TrimSpace(testOut.text) != "" {
		res.TestText = testOut.text
		res.PromptTok += testOut.pt
		res.ComplTok += testOut.ct
		fmt.Printf("%s  ✓ tests ready  %s(%.1fs · %d tokens)%s\n", pColorGold, pColorDim, testOut.elapsed.Seconds(), testOut.pt+testOut.ct, pColorReset)
		res.Stages = append(res.Stages, StageResult{Name: "tests", Model: codeModel, Elapsed: testOut.elapsed, Tokens: testOut.pt + testOut.ct, Status: "ok"})
	} else if opts.SkipTests || opts.TDDFirst {
		res.Stages = append(res.Stages, StageResult{Name: "tests", Status: "skipped"})
	} else if testOut.err != nil {
		res.Stages = append(res.Stages, StageResult{Name: "tests", Model: codeModel, Status: "failed", Error: testOut.err.Error()})
	}

	// ── Stage 4: TEST VERIFICATION (optional) ────────────────────────────────
	// If files were written to disk, run the test suite and iteratively fix
	// failures. Only runs when root is set and a test runner is detected.
	if opts.Root != "" && !opts.SkipTests {
		runPipelineTestLoop(ctx, client, opts.Root, res)
	}

	// BUG-3: commit only after tests pass (or test loop completes).
	if opts.Root != "" && len(res.WrittenFiles) > 0 {
		autoCommitWrites(opts.Root, res.WrittenFiles, res.PlanText)
	}

	res.Combined = assemble(res.PlanText, res.CodeText, res.TestText)

	// 9E: Drop checkpoint stash on successful completion.
	if stashRef != "" {
		gitStashDrop(opts.Root, stashRef)
	}

	// Print failure summary if any stage failed.
	if summary := res.Summary(); summary != "" {
		for _, s := range res.Stages {
			if s.Status == "failed" {
				fmt.Printf("\n%s%s%s", pColorDim, summary, pColorReset)
				break
			}
		}
	}

	// Write pipeline log for debugging/observability.
	if opts.Root != "" {
		writePipelineLog(opts.Root, userRequest, res, time.Since(pipelineStart))
	}

	printDoneLine(time.Since(pipelineStart))
	return res, nil
}

