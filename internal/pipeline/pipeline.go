// ── Progress file (8.5.1) ─────────────────────────────────────────────────────

// writeProgressFile creates or updates .mantis/PROGRESS.md with a structured
// checklist of pipeline tasks. Format mirrors a GitHub issue checklist:
//   [x] Task 1: Setup — done (2026-03-10)
//   [ ] Task 3: Services — pending
//
// Reads back existing file to preserve checkmarks for already-completed tasks
// in case the pipeline is resumed mid-way.
func writeProgressFile(root, userRequest string, tasks []Task) {
	mantisDir := filepath.Join(root, ".mantis")
	if err := os.MkdirAll(mantisDir, 0o755); err != nil {
		return
	}
	progressPath := filepath.Join(mantisDir, "PROGRESS.md")

	var sb strings.Builder
	sb.WriteString("# Mantis — Pipeline Progress\n\n")
	sb.WriteString("**Task:** " + userRequest + "\n\n")
	sb.WriteString("## Tasks\n\n")

	today := time.Now().Format("2006-01-02")
	for i, t := range tasks {
		check := "[ ]"
		suffix := "pending"
		if t.Status == "done" {
			check = "[x]"
			suffix = "done (" + today + ")"
		} else if t.Status == "failed" {
			check = "[!]"
			suffix = "failed"
		}
		sb.WriteString(fmt.Sprintf("- %s Task %d: %s — %s\n", check, i+1, t.Title, suffix))
	}
	sb.WriteString("\n_Updated: " + today + "_\n")

	_ = os.WriteFile(progressPath, []byte(sb.String()), 0o644)
}

// ── 7.6: Git auto-commit after writes ────────────────────────────────────────

// autoCommitWrites creates a git commit for the given files with an auto-generated
// conventional commit message. No-op if MANTIS_NO_AUTOCOMMIT=1, not in a git repo,
// or no files provided.
func autoCommitWrites(root string, files []string, summary string) {
	if os.Getenv("MANTIS_NO_AUTOCOMMIT") == "1" || len(files) == 0 || root == "" {
		return
	}
	// Check if we're in a git repo.
	if err := exec.Command("git", "-C", root, "rev-parse", "--git-dir").Run(); err != nil {
		return
	}
	// Don't auto-commit during a merge/rebase.
	if exec.Command("git", "-C", root, "rev-parse", "--verify", "MERGE_HEAD").Run() == nil {
		return
	}
	// Stash any pre-existing staged changes so we only commit our files.
	// Use git stash --keep-index to preserve working tree, then re-stage only our files.
	// Simpler: just use `git commit -- <files>` which commits only the listed paths.
	args := append([]string{"-C", root, "add", "--"}, files...)
	if err := exec.Command("git", args...).Run(); err != nil {
		return
	}
	// Check if OUR files have staged changes (not the whole index).
	diffArgs := append([]string{"-C", root, "diff", "--cached", "--quiet", "--"}, files...)
	if exec.Command("git", diffArgs...).Run() == nil {
		return // our files have no changes
	}
	// Build commit message from plan summary — skip markdown headers and blank lines.
	msg := fmt.Sprintf("mantis: update %d file(s)", len(files))
	if summary != "" {
		for _, line := range strings.Split(summary, "\n") {
			line = strings.TrimSpace(line)
			if line == "" || strings.HasPrefix(line, "#") || strings.HasPrefix(line, "---") || strings.HasPrefix(line, "```") {
				continue
			}
			if len(line) > 68 {
				line = line[:68] + "..."
			}
			msg = "mantis: " + line
			break
		}
	}
	// Commit only our specific files to avoid including unrelated staged changes.
	commitArgs := append([]string{"-C", root, "commit", "-m", msg, "--"}, files...)
	commitCmd := exec.Command("git", commitArgs...)
	if out, err := commitCmd.CombinedOutput(); err == nil {
		fmt.Printf("%s  ● git: committed %d file(s)%s\n", pColorDim, len(files), pColorReset)
	} else {
		_ = out
	}
}
